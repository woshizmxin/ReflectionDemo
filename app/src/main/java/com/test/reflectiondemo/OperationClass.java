public class OperationClass {
    public float add(int parm1, int parm2) {
        return parm1 + parm2;
    }

    public float cut(int parm1, int parm2) {
        return parm1 - parm2;
    }

    public float ride(int parm1, int parm2) {
        return parm1 * parm2;
    }

    public float Except(int parm1, int parm2) {
        return parm1 / parm2;
    }
}
